#include <bits/stdc++.h>

using namespace std;

int main()
{
    int i;
    long long int lli;
    char c;
    float f;
    double d;
    scanf("%d %lld %c %f %lf", &i, &lli, &c, &f, &d);
    /// printf("%d %lld %c %f %lf\n",i,lli,c,f,d);
    printf("%d\n%lld\n%c\n%0.2f\n%0.1f\n", i, lli, c, f, d);
    return 0;
}